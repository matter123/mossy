#include <math.h>
int abs(int value) {
	return value<0?-value:value;
}
long labs(long value) {
	return value<0?-value:value;
}
long long llabs(long long value) {
	return value<0?-value:value;
}
float fabs(float value) {
	return value<0?-value:value;
}
double dabs(double value) {
	return value<0?-value:value;
}
